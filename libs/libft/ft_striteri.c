void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	idx;

	if (!s || !f)
		return ;
	idx = 0;
	while (s[idx])
	{
		(*f)(idx, &s[idx]);
		idx++;
	}
}
